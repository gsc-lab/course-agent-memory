"""
01_embedding_basics.py — AI Agent 메모리 1단계: 임베딩(Embedding)이란?

[왜 에이전트 메모리에 임베딩이 필요한가]
에이전트는 과거의 대화/사실을 "기억"으로 저장해두고, 새 질문이 들어오면
관련 있는 기억만 골라 떠올려야 한다. 그런데 사람이 키워드로 검색하듯
글자가 똑같은 기억만 찾으면 곤란하다.
  - 저장된 기억: "사용자는 고양이를 키운다"
  - 새 질문:     "반려동물에 대해 뭐라고 했지?"
글자는 하나도 안 겹치지만 의미는 같다. 이런 "의미 기반 검색"을 가능하게
하는 것이 임베딩이다.

[임베딩의 핵심 아이디어]
1) 텍스트를 고정 길이의 숫자 벡터(예: [0.12, -0.04, ...])로 바꾼다.
2) 의미가 비슷한 텍스트는 벡터 공간에서 가까이 위치하도록 만든다.
3) "가깝다"는 보통 코사인 유사도(cosine similarity)로 잰다.

[이 파일의 목표]
진짜 임베딩 모델(OpenAI 등)은 다음 예제에서 쓴다. 여기서는 외부 의존성 없이
'손으로 만든 장난감 임베딩'으로 위 1~3의 메커니즘 자체를 눈으로 확인한다.
원리만 이해하면 실제 모델로 바꿔도 사고방식은 똑같다.
"""

import math

# ---------------------------------------------------------------------------
# 1) 텍스트 -> 벡터
#
# 진짜 임베딩 모델은 신경망이 학습한 의미 공간으로 매핑한다.
# 여기서는 개념 이해용으로 가장 단순한 방식인 "Bag of Words(단어 가방)"를 쓴다.
# 미리 정한 단어 사전(VOCAB)의 각 단어가 문장에 몇 번 등장하는지를 세어
# 그 횟수들을 나열한 것이 벡터다.
#
# (참고) 한국어는 "고양이를", "고양이가"처럼 단어 뒤에 조사가 붙는다. 그래서
# 띄어쓰기로 자른 뒤 정확히 일치하는지 보면 매칭이 안 된다. 이 장난감 버전은
# 단순화를 위해 '사전 단어가 문장 안에 부분 문자열로 몇 번 나오는지'를 센다.
#
# 한계는 명확하다: 사전에 없는 단어는 무시되고, 동의어("고양이"와 "냥이")는
# 아예 매칭되지 않는다. 이 한계를 학습으로 극복한 것이 바로 '진짜' 임베딩
# 모델이라는 점이 이 예제의 핵심이다.
# ---------------------------------------------------------------------------

VOCAB = [
    "고양이", "강아지", "반려동물", "키운다", "좋아한다",
    "파이썬", "자바", "프로그래밍", "언어", "배운다",
]


def embed(text: str) -> list[float]:
    """텍스트를 VOCAB 길이의 벡터로 바꾼다 (사전 단어 등장 횟수 = 차원 값)."""
    return [float(text.count(word)) for word in VOCAB]


# ---------------------------------------------------------------------------
# 2) 두 벡터가 얼마나 비슷한가 — 코사인 유사도
#
# 두 벡터 사이 각도의 코사인 값. 1에 가까울수록 같은 방향(=의미가 비슷),
# 0이면 무관, -1이면 정반대다. 벡터의 '길이(크기)'가 아니라 '방향'만 보기
# 때문에, 문장이 길든 짧든 의미가 같으면 높은 값이 나온다.
# ---------------------------------------------------------------------------

def cosine_similarity(a: list[float], b: list[float]) -> float:
    dot = sum(x * y for x, y in zip(a, b))
    norm_a = math.sqrt(sum(x * x for x in a))
    norm_b = math.sqrt(sum(y * y for y in b))
    if norm_a == 0 or norm_b == 0:  # 사전 단어가 하나도 없는 문장
        return 0.0
    return dot / (norm_a * norm_b)


if __name__ == "__main__":
    print(f"단어 사전(VOCAB) 차원 수: {len(VOCAB)}")
    print(f"VOCAB = {VOCAB}\n")

    # --- 데모 1: 텍스트가 어떻게 벡터로 변하는지 ---
    sample = "고양이를 키운다. 강아지도 좋아한다."
    print(f"[데모 1] 텍스트 -> 벡터")
    print(f"  문장: {sample}")
    print(f"  벡터: {embed(sample)}")
    print("  (VOCAB 순서대로 각 단어 등장 횟수. '고양이'1 '강아지'1 '키운다'1 '좋아한다'1)\n")

    # --- 데모 2: 의미가 비슷한 기억 vs 동떨어진 기억 ---
    # 에이전트가 가진 '기억' 하나를 기준으로, 들어온 질문/문장들과의 유사도를 잰다.
    memory = "사용자는 고양이를 반려동물로 키운다"
    candidates = [
        "그 사람은 반려동물로 고양이를 키운다",   # 같은 의미, 다른 표현
        "강아지를 반려동물로 키운다",             # 비슷한 주제(반려동물)
        "나는 파이썬 프로그래밍 언어를 배운다",   # 완전히 다른 주제
    ]

    mem_vec = embed(memory)
    print(f"[데모 2] 기준 기억과의 의미 유사도")
    print(f"  기준 기억: {memory}\n")

    scored = [(text, cosine_similarity(mem_vec, embed(text))) for text in candidates]
    scored.sort(key=lambda pair: pair[1], reverse=True)  # 유사도 높은 순 = 떠올릴 순서

    for rank, (text, score) in enumerate(scored, start=1):
        print(f"  {rank}위  유사도 {score:.3f}  | {text}")

    print("\n[비교 포인트]")
    print(" - 1위: 글자는 달라도 의미가 같으면 유사도가 가장 높다 (의미 기반 검색의 핵심)")
    print(" - 2위: '반려동물'은 겹치지만 동물이 달라 1위보다 낮다")
    print(" - 3위: 공유 단어가 없어 유사도 0 — 에이전트라면 이 기억은 안 떠올린다")
    print(" - 다음 예제: 이 장난감 embed()를 진짜 임베딩 모델로 교체하면,")
    print("   '냥이'/'고양이'처럼 단어가 달라도 가까운 벡터가 나오게 된다")
